// Returns the [fromYear, toYear] generation range that contains the given year,
// or null if no range data exists (caller falls back to exact-year matching).
function getVehicleYearRange(make, model, year) {
    const yr = Number(year);
    if (!yr || !make || !model) return null;
    const ranges = VEHICLE_YEAR_RANGES[make]?.[model];
    if (!ranges) return null;
    return ranges.find(([from, to]) => yr >= from && yr <= to) || null;
}

const VEHICLE_MAKES = Object.keys(VEHICLE_DB).sort();

function getVehicleModels(make) {
    const all = VEHICLE_DB[make] || [];
    // Exclude series entries — any entry that is a sub-model of another entry
    // e.g. "HiLux N70" is excluded because "HiLux" exists in the same list
    const base = all.filter(m => !all.some(b => b !== m && m.startsWith(b + ' ')));
    return base.slice().sort();
}

function buildYearOptions(selectedYear) {
    const current = new Date().getFullYear();
    let html = '<option value="">Year</option>';
    for (let y = current + 1; y >= 1950; y--) {
        html += `<option value="${y}"${selectedYear == y ? ' selected' : ''}>${y}</option>`;
    }
    return html;
}

// Trim the year list to the min–max span of a model's known generations.
// Falls back to the full list if no range data exists for this make/model.
function buildYearOptionsForModel(make, model, selectedYear) {
    if (!make || !model) return buildYearOptions(selectedYear);
    let ranges = VEHICLE_YEAR_RANGES?.[make]?.[model];
    // No direct range — aggregate from all sub-series (e.g. "HiLux" → all "HiLux *" entries)
    if (!ranges?.length) {
        const prefix = model + ' ';
        const sub = Object.entries(VEHICLE_YEAR_RANGES?.[make] || {})
            .filter(([k]) => k.startsWith(prefix))
            .flatMap(([, r]) => r);
        if (sub.length) ranges = sub;
    }
    if (!ranges || !ranges.length) return buildYearOptions(selectedYear);
    const minYear = Math.min(...ranges.map(([f]) => f));
    const maxYear = Math.max(...ranges.map(([, t]) => t));
    const cap = new Date().getFullYear() + 1;
    const top = Math.min(maxYear, cap);
    // If a previously-selected year is now out of range, drop it silently
    const sel = (selectedYear && Number(selectedYear) >= minYear && Number(selectedYear) <= maxYear)
        ? selectedYear : '';
    let html = '<option value="">Year</option>';
    for (let y = top; y >= minYear; y--) {
        html += `<option value="${y}"${sel == y ? ' selected' : ''}>${y}</option>`;
    }
    return html;
}

function buildMakeOptions(selectedMake) {
    let html = '<option value="">Make</option>';
    VEHICLE_MAKES.forEach(make => {
        html += `<option value="${make}"${selectedMake === make ? ' selected' : ''}>${make}</option>`;
    });
    return html;
}

function buildModelOptions(make, selectedModel) {
    const models = getVehicleModels(make);
    let html = '<option value="">Model</option>';
    models.forEach(model => {
        html += `<option value="${model}"${selectedModel === model ? ' selected' : ''}>${model}</option>`;
    });
    return html;
}

// Returns series options for models that have named generations (e.g. Commodore VB, Falcon XA, 3 Series E30).
// Returns '' if no series data exists for this make/model combination.
function buildSeriesOptions(make, model, year, selected) {
    if (!make || !model) return '';
    const prefix = model + ' ';
    let seriesList = (VEHICLE_DB[make] || [])
        .filter(m => m.startsWith(prefix))
        .map(m => m.slice(prefix.length));
    if (year && seriesList.length) {
        const yr = parseInt(year);
        seriesList = seriesList.filter(s => {
            const ranges = VEHICLE_YEAR_RANGES[make]?.[model + ' ' + s];
            if (!ranges) return true;
            return ranges.some(([start, end]) => yr >= start && yr <= end);
        });
    }
    if (!seriesList.length) return '';
    let html = '<option value="">Select series</option>';
    seriesList.forEach(s => {
        const ranges = VEHICLE_YEAR_RANGES[make]?.[model + ' ' + s];
        let label = s;
        if (ranges?.length) {
            const minY = Math.min(...ranges.map(([f]) => f));
            const maxY = Math.max(...ranges.map(([, t]) => t));
            label = `${s} (${minY}–${maxY})`;
        }
        html += `<option value="${s}"${s === selected ? ' selected' : ''}>${label}</option>`;
    });
    return html;
}
